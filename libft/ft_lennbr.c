int	ft_lennbr(int n)
{
	int	len;

	if (n == -2147483648)
		return (11);
	len = 1;
	if (n < 0)
	{
		n *= -1;
		len++;
	}
	while (n > 9)
	{
		n /= 10;
		len++;
	}
	return (len);
}
